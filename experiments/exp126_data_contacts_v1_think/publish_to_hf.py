"""Publish the think-augmented contacts-v1 corpus to the open-athena/MarinFold HF bucket.

Reproducible rebuild-and-upload (AGENTS.md): scans the locally generated
`documents/<split>/` shards for the corpus statistics, renders the dataset
`README.md`, builds the (unchanged) contacts-v1 tokenizer, and syncs
everything to
`hf://buckets/open-athena/MarinFold/data/document_structures/contacts_v1_think/`.

Usage::

    export HF_TOKEN=...            # an open-athena-scoped token
    python publish_to_hf.py --documents ~/exp126_scratch/documents [--stats-only]
"""

import argparse
import json
import subprocess
import sys
from collections import Counter
from pathlib import Path

import pyarrow.parquet as pq

BUCKET = "hf://buckets/open-athena/MarinFold/data/document_structures/contacts_v1_think"
SPLITS = ("train", "val", "test")
EXPECTED_SHARDS = {"train": 2067, "val": 22, "test": 22}


def scan_split(split_dir: Path) -> dict:
    """Aggregate per-split stats over the local shards (small columns only)."""
    shards = sorted(split_dir.glob("contacts_v1-*.parquet"))
    docs = truncated = think_docs = 0
    tokens = think_tokens = 0
    max_tokens = 0
    by_round: Counter = Counter()
    for sh in shards:
        t = pq.read_table(sh, columns=["num_tokens", "think_tokens", "round", "truncated"])
        nt = t.column("num_tokens").to_pylist()
        tt = t.column("think_tokens").to_pylist()
        rd = t.column("round").to_pylist()
        tr = t.column("truncated").to_pylist()
        docs += len(nt)
        tokens += sum(nt)
        think_tokens += sum(x or 0 for x in tt)
        think_docs += sum(1 for x in tt if x)
        truncated += sum(1 for x in tr if x)
        max_tokens = max(max_tokens, max(nt) if nt else 0)
        by_round.update(rd)
    return {
        "shards": len(shards),
        "docs": docs,
        "tokens": tokens,
        "think_tokens": think_tokens,
        "think_docs": think_docs,
        "truncated": truncated,
        "max_tokens": max_tokens,
        "by_round": {int(k): int(v) for k, v in sorted(by_round.items())},
    }


def render_readme(stats: dict, generator_rev: str) -> str:
    total_docs = sum(s["docs"] for s in stats.values())
    total_tokens = sum(s["tokens"] for s in stats.values())
    total_think = sum(s["think_tokens"] for s in stats.values())
    train = stats["train"]

    def round_row(split: str) -> str:
        r = stats[split]["by_round"]
        cells = " | ".join(f"{r.get(i, 0):,}" for i in range(5))
        return f"| {split} | {cells} | {stats[split]['docs']:,} |"

    rows = "\n".join(round_row(s) for s in SPLITS)
    think_frac = 100 * sum(s["think_docs"] for s in stats.values()) / total_docs
    mean_think = total_think / total_docs
    mean_tokens = total_tokens / total_docs
    return f"""# contacts-v1-think protein-document dataset

Training documents in the **contacts-v1** format **with `<think>` (pause) tokens**
in the structure section — a think-augmented twin of
[`contacts_v1`](../contacts_v1) over the **same** afdb-24M entries, clusters,
pLDDT rounds, and train/val/test splits. Generated by calling MarinFold's
`contacts_v1` generator with `GenerationConfig(think=True)` (no
re-implementation). Format spec + think details:
[`marinfold/.../document_structures/contacts_v1/SPEC.md`](https://github.com/Open-Athena/MarinFold/blob/main/marinfold/marinfold/document_structures/contacts_v1/SPEC.md)
(§ *Think (pause) tokens*).

Produced by experiment [exp126](https://github.com/Open-Athena/MarinFold/issues/126)
(`marinfold` @ `{generator_rev}`; think path from
[#123](https://github.com/Open-Athena/MarinFold/issues/123) / PR #125). Intended
as the training corpus for [#124](https://github.com/Open-Athena/MarinFold/issues/124).

## What `<think>` means here

`<think>` runs are inserted **between `<contact>` statements** in the structure
section (never inside one), sampled per #123's spec: an initial run right after
`<begin_statements>` (P=0.75, `Geometric(0.13)`), plus `max(int(U(-4,4)), 0)`
extra runs each `Geometric(0.25)`, with the total subtracted from the 8192-token
budget before contacts are selected. **No vocab/tokenizer change** — `<think>`
was already reserved in the contacts-v1 vocab. At training time, **mask the loss
on `<think>` tokens** (they carry no target). The `think_tokens` column gives the
per-document count.

## Structure (rounds)

Identical selection to [`contacts_v1`](../contacts_v1): up to **5 examples per
structural cluster** by pLDDT (`round = 0` highest), clusters with **< 3 usable
members dropped**, shards laid out **round-descending** (round-4 first … round-0
last) so a sequential reader trains on the highest-quality data last. The
`round` column is authoritative regardless of file order. `split` is inherited
from afdb-24M and is cluster-consistent (no train/test leakage).

## Layout

```
<split>/contacts_v1-{{shard:05d}}-of-{{total:05d}}.parquet   # round-descending, matches contacts_v1 shard-for-shard
tokenizer/                                                   # contacts-v1 tokenizer (unchanged from contacts_v1)
```

## Counts

**{total_docs:,} documents** over the same 960,054 structural clusters (0 generation drops).

| split | round 0 | round 1 | round 2 | round 3 | round 4 | total |
|---|--:|--:|--:|--:|--:|--:|
{rows}

**~{total_tokens/1e9:.2f} B tokens total** (mean ~{mean_tokens:,.0f} `num_tokens`/doc, max {train['max_tokens']:,}). **{total_think:,} `<think>` tokens** across the corpus (~{think_frac:.0f}% of docs carry a think run; mean ~{mean_think:.1f} think tokens/doc).

## Columns

Same schema as [`contacts_v1`](../contacts_v1) **plus `think_tokens`**:
`document` (token string) · `structure` (`"contacts-v1"`) · `entry_id` · `round` ·
`struct_cluster_id` · `seq_cluster_id` · `split` · `global_plddt` · `seq_len` ·
`start_index` / `n_term_index` / `c_term_index` · `contacts_pre_filter` /
`contacts_passing_min_degree` / `contacts_emitted` / `contacts_excluded` ·
`truncated` · `highest_contact_degree` / `lowest_nonzero_contact_degree` /
`lowest_included_contact_degree` · `num_tokens` · **`think_tokens`** · `sha1` ·
`uniprot_accession` / `tax_id` / `organism_name`.

`sha1` = sha1 of `document`, so byte-equality with the MarinFold generator is a single column compare.
"""


def _run(cmd: list[str]) -> None:
    print("+", " ".join(cmd), flush=True)
    subprocess.run(cmd, check=True)


def main(argv: list[str] | None = None) -> int:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--documents", type=Path, required=True,
                    help="Local dir with <split>/contacts_v1-*.parquet.")
    ap.add_argument("--generator-rev", default=None,
                    help="marinfold git rev used for generation (default: git HEAD).")
    ap.add_argument("--stats-only", action="store_true",
                    help="Compute + print stats and render README, but do not upload.")
    ap.add_argument("--skip-tokenizer", action="store_true")
    args = ap.parse_args(argv)

    # Verify shard completeness before publishing anything.
    stats = {}
    for split in SPLITS:
        s = scan_split(args.documents / split)
        stats[split] = s
        exp = EXPECTED_SHARDS[split]
        flag = "OK" if s["shards"] == exp else f"!! expected {exp}"
        print(f"[{split}] {s['shards']} shards {flag} | {s['docs']:,} docs | "
              f"{s['tokens']/1e9:.3f}B tokens | think_docs={s['think_docs']:,} | "
              f"truncated={s['truncated']:,} | rounds={s['by_round']}", flush=True)
        if s["shards"] != exp:
            print(f"ERROR: {split} incomplete ({s['shards']}/{exp}); not publishing.",
                  file=sys.stderr)
            return 1

    rev = args.generator_rev
    if rev is None:
        rev = subprocess.run(["git", "rev-parse", "--short", "HEAD"],
                             capture_output=True, text=True).stdout.strip() or "unknown"
    readme = render_readme(stats, rev)
    out_readme = args.documents / "README.md"
    out_readme.write_text(readme)
    (args.documents / "stats.json").write_text(json.dumps(stats, indent=2))
    print(f"\nWrote {out_readme} and stats.json", flush=True)

    if args.stats_only:
        print("--stats-only: skipping upload.")
        return 0

    # Build the (unchanged) contacts-v1 tokenizer locally.
    tok_dir = args.documents / "tokenizer"
    if not args.skip_tokenizer:
        from marinfold import build_tokenizer
        from marinfold.document_structures.contacts_v1.vocab import all_domain_tokens
        tok_dir.mkdir(exist_ok=True)
        build_tokenizer(all_domain_tokens()).save_pretrained(str(tok_dir))
        print(f"Built tokenizer → {tok_dir}", flush=True)

    # Sync shards per split, then upload README + tokenizer.
    for split in SPLITS:
        _run(["hf", "buckets", "sync", str(args.documents / split),
              f"{BUCKET}/{split}"])
    _run(["hf", "buckets", "cp", str(out_readme), f"{BUCKET}/README.md"])
    if not args.skip_tokenizer:
        _run(["hf", "buckets", "sync", str(tok_dir), f"{BUCKET}/tokenizer"])
    print("\nPublished to", BUCKET, flush=True)
    return 0


if __name__ == "__main__":
    sys.exit(main())
