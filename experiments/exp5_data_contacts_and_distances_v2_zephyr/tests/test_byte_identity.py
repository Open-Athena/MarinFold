"""Byte-identity test: exp5 generates the same docs as exp34's reference.

exp5 re-implements parse + generate for performance, with a fresh
columnar ParsedStructure that's not interchangeable with exp34's
tuple-based one. The contract that keeps the docs aligned is that the
RNG stream order is unchanged and the FP arithmetic is reproduced
exactly (see ``parse.py`` and ``generate.py`` docstrings for the
specifics: left-fold pLDDT sum, np.sqrt over float64 = math.sqrt on
the same floats, np.triu_indices preserves the legacy row-major (i, j)
iteration order, etc.).

This test pins that contract: a synthetic structure through several
distinct entry_ids must produce the same doc bytes whether parsed +
generated by exp34 or by exp5. Any drift will surface here.

The exp34 reference is imported by temporarily swapping
``sys.path`` — it lives in a sibling experiment dir, not as a
package.
"""

from __future__ import annotations

import dataclasses
import hashlib
import importlib
import sys
from pathlib import Path

import pytest

import generate as exp5_generate
import parse as exp5_parse

_EXP34_DIR = Path(__file__).resolve().parents[2] / "exp34_document_structures_contacts_and_distances_v2"


def _import_exp34():
    """Import exp34's parse + generate under aliases, isolated from exp5's modules."""
    # Save exp5's modules under their original names, swap exp34 in, import,
    # then restore. This way both versions coexist for the duration of the test.
    saved = {name: sys.modules[name] for name in ("parse", "generate", "vocab")
             if name in sys.modules}
    # exp34 is already on sys.path (added in conftest), but its imports
    # ("from parse import …" in generate.py) need to resolve to exp34's
    # parse, not exp5's. Force re-import in the right path order.
    for name in ("parse", "generate", "vocab"):
        sys.modules.pop(name, None)
    sys.path.insert(0, str(_EXP34_DIR))
    try:
        exp34_parse = importlib.import_module("parse")
        exp34_generate = importlib.import_module("generate")
        return exp34_parse, exp34_generate
    finally:
        sys.path.remove(str(_EXP34_DIR))
        # Restore exp5's modules so subsequent tests see the right ones.
        for name in ("parse", "generate", "vocab"):
            sys.modules.pop(name, None)
        for name, mod in saved.items():
            sys.modules[name] = mod


@pytest.mark.parametrize("entry_id", [
    "AF-A0A090SHW3-F1",
    "AF-H0GCG4-F1",
    "AF-K7EXM5-F1",
    "AF-XYZ123-F1",
    "AF-TESTSEED-F2",
])
def test_doc_byte_identical_to_exp34(synthetic_cif, entry_id, tmp_path):
    """Same cif + same entry_id → exp34 and exp5 produce the same doc string.

    The structure is fixed; ``entry_id`` is what seeds the RNG, so varying
    it across the parametrize sweep exercises a few different statement
    orderings + think-overhead samples + pLDDT placements.

    exp34's ``parse_structure`` reads from a ``Path`` and derives entry_id
    from the file stem; we write the synthetic cif to a tmpfile, parse it,
    then ``dataclasses.replace`` to swap in our parametrized entry_id —
    keeps the cif content identical across both sides so any diff is
    parse/generate logic, not source data.
    """
    cif_path = tmp_path / f"{entry_id}.cif"
    cif_path.write_text(synthetic_cif)

    cfg5 = exp5_generate.GenerationConfig()
    ps5 = exp5_parse.parse_cif_content(synthetic_cif, entry_id=entry_id)
    doc5 = exp5_generate.generate_one(ps5, context_length=8192, cfg=cfg5)

    exp34_parse, exp34_generate = _import_exp34()
    cfg34 = exp34_generate.GenerationConfig()
    ps34_raw = exp34_parse.parse_structure(cif_path)
    ps34 = dataclasses.replace(ps34_raw, entry_id=entry_id)
    doc34 = exp34_generate._generate_one(ps34, context_length=8192, cfg=cfg34)

    assert doc5 is not None, "exp5 returned None"
    assert doc34 is not None, "exp34 returned None"
    assert doc5 == doc34, (
        f"docs diverge for entry_id={entry_id}\n"
        f"  exp5 sha1: {hashlib.sha1(doc5.encode()).hexdigest()[:16]} len={len(doc5)}\n"
        f"  exp34 sha1: {hashlib.sha1(doc34.encode()).hexdigest()[:16]} len={len(doc34)}\n"
    )
