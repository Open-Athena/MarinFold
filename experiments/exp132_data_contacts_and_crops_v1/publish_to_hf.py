"""Publish the contacts-and-crops-v1 corpus to the open-athena/MarinFold HF bucket.

Reproducible rebuild-and-upload (AGENTS.md): scans the locally generated
`documents/<split>/` shards for corpus statistics, renders the dataset
`README.md`, builds the contacts-and-crops-v1 tokenizer, and syncs everything
to
`hf://buckets/open-athena/MarinFold/data/document_structures/contacts_and_crops_v1/`.

Usage::

    export HF_TOKEN=...            # an open-athena-scoped token
    python publish_to_hf.py --documents /data/exp132_contacts_and_crops_v1_scratch/documents \
        --hf-bin /home/bizon/anaconda3/bin/hf [--stats-only]
"""

import argparse
import json
import subprocess
import sys
from collections import Counter
from pathlib import Path

import pyarrow.parquet as pq

BUCKET = "hf://buckets/open-athena/MarinFold/data/document_structures/contacts_and_crops_v1"
SPLITS = ("train", "val", "test")
EXPECTED_SHARDS = {"train": 2067, "val": 22, "test": 22}


def scan_split(split_dir: Path) -> dict:
    """Aggregate per-split stats over the local shards (small columns only)."""
    shards = sorted(split_dir.glob("contacts_and_crops_v1-*.parquet"))
    docs = truncated = 0
    tokens = pass1 = crops = empty_crops = crop_atoms = 0
    max_tokens = max_box_visits = 0
    by_round: Counter = Counter()
    cols = ["num_tokens", "round", "truncated", "num_pass1_mentions",
            "num_crops", "num_empty_crops", "crop_atoms_emitted", "max_box_visits"]
    for sh in shards:
        t = pq.read_table(sh, columns=cols)
        nt = t.column("num_tokens").to_pylist()
        docs += len(nt)
        tokens += sum(nt)
        max_tokens = max(max_tokens, max(nt) if nt else 0)
        truncated += sum(1 for x in t.column("truncated").to_pylist() if x)
        pass1 += sum(t.column("num_pass1_mentions").to_pylist())
        crops += sum(t.column("num_crops").to_pylist())
        empty_crops += sum(t.column("num_empty_crops").to_pylist())
        crop_atoms += sum(t.column("crop_atoms_emitted").to_pylist())
        mbv = t.column("max_box_visits").to_pylist()
        max_box_visits = max(max_box_visits, max(mbv) if mbv else 0)
        by_round.update(t.column("round").to_pylist())
    return {
        "shards": len(shards),
        "docs": docs,
        "tokens": tokens,
        "truncated": truncated,
        "max_tokens": max_tokens,
        "pass1_mentions": pass1,
        "crops": crops,
        "empty_crops": empty_crops,
        "crop_atoms": crop_atoms,
        "max_box_visits": max_box_visits,
        "by_round": {int(k): int(v) for k, v in sorted(by_round.items())},
    }


def render_readme(stats: dict, generator_rev: str) -> str:
    total_docs = sum(s["docs"] for s in stats.values())
    total_tokens = sum(s["tokens"] for s in stats.values())
    total_crops = sum(s["crops"] for s in stats.values())
    total_empty = sum(s["empty_crops"] for s in stats.values())
    total_crop_atoms = sum(s["crop_atoms"] for s in stats.values())
    total_pass1 = sum(s["pass1_mentions"] for s in stats.values())
    train = stats["train"]
    mean_tokens = total_tokens / total_docs
    mean_crops = total_crops / total_docs
    empty_frac = 100 * total_empty / total_crops if total_crops else 0

    def round_row(split: str) -> str:
        r = stats[split]["by_round"]
        cells = " | ".join(f"{r.get(i, 0):,}" for i in range(5))
        return f"| {split} | {cells} | {stats[split]['docs']:,} |"

    rows = "\n".join(round_row(s) for s in SPLITS)
    return f"""# contacts-and-crops-v1 protein-document dataset

Training documents in the **contacts-and-crops-v1** format — a
*coordinate*-bearing format that fits an **8192-token** context (like the
contacts formats), over the **same** afdb-24M entries, clusters, pLDDT rounds,
and train/val/test splits as [`contacts_v1`](../contacts_v1) and
[`contacts_and_coordinates_v1`](../contacts_and_coordinates_v1) (ccoord).
Generated by calling MarinFold's `contacts_and_crops_v1.generate_document`
(no re-implementation). Format spec:
[`marinfold/.../document_structures/contacts_and_crops_v1/SPEC.md`](https://github.com/Open-Athena/MarinFold/blob/main/marinfold/marinfold/document_structures/contacts_and_crops_v1/SPEC.md).

Produced by experiment [exp132](https://github.com/Open-Athena/MarinFold/issues/132)
(`marinfold` @ `{generator_rev}`; format from
[#130](https://github.com/Open-Athena/MarinFold/issues/130) / PR #131).

## The format, briefly

contacts-v1's sequence section and `<contact>` statements verbatim, then a
two-pass coordinate section: **Pass 1** gives every atom a cheap 10 Å box
(`<pX> <ATOM> <xyz-HHH> <xyz-TTT>`), then **Pass 2** reveals 0.1 Å detail
inside a handful of `<crop>` boxes. Fits 8192 tokens for every chain; small/mid
proteins reach near-full 0.1 Å structure, large proteins a coarse sketch plus
scattered fine crops. Positions live in a random rotated + translated frame
(free augmentation). Only two tokens are new vs ccoord
(`<contacts-and-crops-v1>`, `<crop>`); the `<xyz-DDD>` vocab is shared, so a
model warm-starts from a contacts-v1 / ccoord checkpoint.

## Structure (rounds)

Identical selection to [`contacts_v1`](../contacts_v1): up to **5 examples per
structural cluster** by pLDDT (`round = 0` highest), clusters with **< 3 usable
members dropped**, shards laid out **round-descending** so a sequential reader
trains on the highest-quality data last. The `round` column is authoritative
regardless of file order. `split` is inherited from afdb-24M and is
cluster-consistent (no train/test leakage).

## Layout

```
<split>/contacts_and_crops_v1-{{shard:05d}}-of-{{total:05d}}.parquet   # round-descending, aligns shard-for-shard with contacts_v1
tokenizer/                                                             # contacts-and-crops-v1 tokenizer (3846-token domain vocab)
```

## Counts

**{total_docs:,} documents** over the same 960,054 structural clusters.

| split | round 0 | round 1 | round 2 | round 3 | round 4 | total |
|---|--:|--:|--:|--:|--:|--:|
{rows}

**~{total_tokens/1e9:.1f} B tokens total** (mean ~{mean_tokens:,.0f} `num_tokens`/doc, max {train['max_tokens']:,} — the format is budget-filling at 8192). Per doc: ~{total_pass1/total_docs:,.0f} Pass-1 coarse-box mentions and ~{mean_crops:.1f} crops ({empty_frac:.0f}% of crops empty — the surface shell), ~{total_crop_atoms/total_docs:,.0f} atoms at 0.1 Å.

## Columns

`document` (token string) · `structure` (`"contacts-and-crops-v1"`) · `entry_id` ·
`round` · `struct_cluster_id` · `seq_cluster_id` · `split` · `global_plddt` ·
`seq_len` · `start_index` / `n_term_index` / `c_term_index` ·
`min_seq_separation` · `num_contacts_eligible` / `contacts_emitted` ·
`num_eligible_atoms` · `num_pass1_mentions` · `num_crops` / `num_empty_crops` /
`num_distinct_crop_boxes` / `crop_atoms_emitted` / `max_box_visits` ·
`quat_w`/`quat_x`/`quat_y`/`quat_z` · `trans_x`/`trans_y`/`trans_z` ·
`truncated` · `num_tokens` · `sha1` · `uniprot_accession` / `tax_id` /
`organism_name`.

`sha1` = sha1 of `document`, so byte-equality with the MarinFold generator is a single column compare.
"""


def _run(cmd: list[str]) -> None:
    print("+", " ".join(cmd), flush=True)
    subprocess.run(cmd, check=True)


def main(argv: list[str] | None = None) -> int:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--documents", type=Path, required=True,
                    help="Local dir with <split>/contacts_and_crops_v1-*.parquet.")
    ap.add_argument("--generator-rev", default=None,
                    help="marinfold git rev used for generation (default: git HEAD).")
    ap.add_argument("--stats-only", action="store_true",
                    help="Compute + print stats and render README, but do not upload.")
    ap.add_argument("--skip-tokenizer", action="store_true")
    ap.add_argument("--hf-bin", default="hf",
                    help="Path to an `hf` CLI with the `buckets` subcommand "
                         "(needs huggingface_hub>=1.5; the marinfold venv pins "
                         "0.36 which lacks it, so pass the workstation hf, e.g. "
                         "/home/bizon/anaconda3/bin/hf).")
    args = ap.parse_args(argv)

    # Verify shard completeness before publishing anything.
    stats = {}
    for split in SPLITS:
        s = scan_split(args.documents / split)
        stats[split] = s
        exp = EXPECTED_SHARDS[split]
        flag = "OK" if s["shards"] == exp else f"!! expected {exp}"
        print(f"[{split}] {s['shards']} shards {flag} | {s['docs']:,} docs | "
              f"{s['tokens']/1e9:.3f}B tokens | crops={s['crops']:,} | "
              f"truncated={s['truncated']:,} | rounds={s['by_round']}", flush=True)
        if s["shards"] != exp:
            print(f"ERROR: {split} incomplete ({s['shards']}/{exp}); not publishing.",
                  file=sys.stderr)
            return 1

    rev = args.generator_rev
    if rev is None:
        rev = subprocess.run(["git", "rev-parse", "--short", "HEAD"],
                             capture_output=True, text=True).stdout.strip() or "unknown"
    readme = render_readme(stats, rev)
    out_readme = args.documents / "README.md"
    out_readme.write_text(readme)
    (args.documents / "stats.json").write_text(json.dumps(stats, indent=2))
    print(f"\nWrote {out_readme} and stats.json", flush=True)

    if args.stats_only:
        print("--stats-only: skipping upload.")
        return 0

    # Build the contacts-and-crops-v1 tokenizer locally.
    tok_dir = args.documents / "tokenizer"
    if not args.skip_tokenizer:
        from marinfold import build_tokenizer
        from marinfold.document_structures.contacts_and_crops_v1.vocab import (
            all_domain_tokens,
        )
        tok_dir.mkdir(exist_ok=True)
        build_tokenizer(all_domain_tokens()).save_pretrained(str(tok_dir))
        print(f"Built tokenizer → {tok_dir}", flush=True)

    # Sync shards per split, then upload README + tokenizer.
    hf = args.hf_bin
    for split in SPLITS:
        _run([hf, "buckets", "sync", str(args.documents / split),
              f"{BUCKET}/{split}"])
    _run([hf, "buckets", "cp", str(out_readme), f"{BUCKET}/README.md"])
    if not args.skip_tokenizer:
        _run([hf, "buckets", "sync", str(tok_dir), f"{BUCKET}/tokenizer"])
    print("\nPublished to", BUCKET, flush=True)
    return 0


if __name__ == "__main__":
    sys.exit(main())
